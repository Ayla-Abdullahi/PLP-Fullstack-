name = "Ayla"
print(len(name))  # Output: 4
print(name.capitalize())  # Output: Ayla
print(name.upper())  # Output: AYLA
print(name.lower())  # Output: ayla
print(name.find("A"))  # Output: 0
print(name.replace("A", "E"))  # Output: Eyla
print(name.isdigit())  # Output: False
print(name.isalpha())  # Output: True